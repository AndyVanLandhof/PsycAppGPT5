// Helper function to get all questions for a year
export const getQuestionsForYear = (year) => {
  return HAMLET_PAST_PAPERS[year]?.questions || [];
};

// Helper function to get all years
export const getAvailableYears = () => {
  return Object.keys(HAMLET_PAST_PAPERS).map(Number).sort((a, b) => b - a);
};

// Helper function to get a specific question
export const getQuestion = (year, questionNumber) => {
  const yearData = HAMLET_PAST_PAPERS[year];
  if (!yearData) return null;
  return yearData.questions.find(q => q.questionNumber === questionNumber);
};

